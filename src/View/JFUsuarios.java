    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        DefaultTableModel dtm = (DefaultTableModel) tblUsuarios.getModel();
        int linhaSelecionada = tblUsuarios.getSelectedRow();
        if (linhaSelecionada != -1) {
            txtNome.setText(String.valueOf(dtm.getValueAt(linhaSelecionada, 1)));
            txtUsuario.setText(String.valueOf(dtm.getValueAt(linhaSelecionada, 2)));
            txtSenha.setText(String.valueOf(dtm.getValueAt(linhaSelecionada, 3)));
            txtConfSenha.setText(String.valueOf(dtm.getValueAt(linhaSelecionada, 3)));
        }
    }//GEN-LAST:event_tblUsuariosMouseClicked

