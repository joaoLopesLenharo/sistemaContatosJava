    public void carregaUsers() {
        UsuarioDAO dao = new UsuarioDAO();
        List<Usuario> user = dao.getLista();;
        DefaultTableModel dtm = (DefaultTableModel) tblUsuarios.getModel();
        dtm.setRowCount(0);
        for (Usuario u : user) {
            Object[] registro = new Object[]{
                u.getId(),
                u.getNome(),
                u.getUsuario(),
                u.getSenha()
            };
            dtm.addRow(registro);
        }
    }

    public void pesquisaUsuarios() {
        UsuarioDAO dao = new UsuarioDAO();
        String nome = txtPesquisar.getText();
        List<Usuario> usuarios = dao.pesquisaNome(nome);
        DefaultTableModel dtm = (DefaultTableModel) tblUsuarios.getModel();
        dtm.setRowCount(0);
        for (Usuario u : usuarios) {
            Object[] registro = new Object[]{
                u.getId(),
                u.getNome(),
                u.getUsuario(),
                u.getSenha()
            };
            dtm.addRow(registro);
        }
    }

