    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (!txtNome.getText().isEmpty() && !txtUsuario.getText().isEmpty() && !String.valueOf(txtSenha.getPassword()).isEmpty() && !String.valueOf(txtConfSenha.getPassword()).isEmpty()) {
            if (String.valueOf(txtSenha.getPassword()).equals(String.valueOf(txtSenha.getPassword()))) {
                DefaultTableModel dtm = (DefaultTableModel) tblUsuarios.getModel();
                int linhaSelecionada = tblUsuarios.getSelectedRow();
                if (linhaSelecionada != -1) {
                    Usuario u1 = new Usuario();
                    u1.setNome(txtNome.getText());
                    u1.setUsuario(txtUsuario.getText());
                    u1.setSenha(String.valueOf(txtSenha.getPassword()));
                    u1.setId((long) dtm.getValueAt(linhaSelecionada, 0));
                    //System.out.println("ssadsadsadsadsadsadsad");
                    UsuarioDAO dao = new UsuarioDAO();
                    dao.atualiza(u1);
                    carregaUsers();
                    limpaCampos();
                } else {
                    Usuario u1 = new Usuario();
                    u1.setNome(txtNome.getText());
                    u1.setUsuario(txtUsuario.getText());
                    u1.setSenha(String.valueOf(txtSenha.getPassword()));

                    UsuarioDAO dao = new UsuarioDAO();
                    dao.adiciona(u1);
                    carregaUsers();
                    limpaCampos();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Os campos de senha não são correspondentes!", "Alerta!", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Há campos obrigatórios que não foram preenchidos!", "Alerta!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int n = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja remover o contato?", "ATENÇÃO!", JOptionPane.YES_NO_OPTION);
        //System.out.println(n);
        if (n == 0) {
            UsuarioDAO dao = new UsuarioDAO();
            DefaultTableModel dtm = (DefaultTableModel) tblUsuarios.getModel();
            int linhaSelecionada = tblUsuarios.getSelectedRow();
            long id = (long) dtm.getValueAt(linhaSelecionada, 0);
            dao.deletaUsuario(id);
            carregaUsers();
            limpaCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Operação cancelada!");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

